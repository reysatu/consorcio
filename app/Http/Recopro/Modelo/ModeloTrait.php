namespace App\Http\Recopro\Modelo;

use Carbon\Carbon;

trait ModeloTrait
{
    public function generateDataExcel($info)
    {
        $columns[] = ['MODELO','MARCA','ESTADO','U.CREADO', 'F.CREADO', 'U.MODIFICADO', 'F.MODIFICADO'];

        foreach ($info as $i) {
            $estado="ACTIVO";
            if($i->estado=='I'){
                $estado='INACTIVO';
            };
            $columns[] = [
                ['left', $i->descripcion],
                ['left',$i->Brand_d->description],
                ['left', $estado],
                ['left', $i->user_c->name],
                ['center', (Carbon::parse($i->created_at)->format('d-m-Y'))],
                ['left', $i->user_u->name],
                ['center', (Carbon::parse($i->updated_at)->format('d-m-Y'))]
            ];
        }

        $data = [
            'data' => $columns,
            'title' => 'LISTA DE MODELOS'
        ];

        return $data;
    }
}